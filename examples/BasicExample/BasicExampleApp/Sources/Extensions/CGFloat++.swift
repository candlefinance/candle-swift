import CoreGraphics

/// A collection of common spacing and padding values used throughout the app.
extension CGFloat {

    /// A very small spacing value (2 points).
    static let xSmall: CGFloat = 2

    /// A very small spacing value (4 points).
    static let small: CGFloat = 4

    /// A small spacing value (8 points).
    static let medium: CGFloat = 8

    /// A small spacing value (12 points).
    static let mediumPlus: CGFloat = 12

    /// A standard spacing value (16 points).
    static let large: CGFloat = 16

    /// A standard spacing value (20 points).
    static let larger: CGFloat = 20

    /// A slightly larger spacing value (24 points).
    static let extraLarge: CGFloat = 24

    /// A slightly larger spacing value (28 points).
    static let extraLarger: CGFloat = 28

    /// A medium-large spacing value (32 points).
    static let extraExtraLarge: CGFloat = 32

    /// A large spacing value (40 points).
    static let huge: CGFloat = 40

    /// An extra large spacing value (48 points).
    static let extraHuge: CGFloat = 48

    /// A very large spacing value (64 points).
    static let massive: CGFloat = 64

    /// The largest predefined spacing value (96 points).
    static let ultraMassive: CGFloat = 96
}
